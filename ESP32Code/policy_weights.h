/* ---------- Forward pass ---------- */
static void policy_forward(const float* obs_raw, float* out_probs) {
    float buf0[POLICY_INPUT_DIM];
    float buf1[512];   // ara katman bufferı (en büyük katman için yeterli)

    /* Normalise: x = (raw - low) / (high - low) * 2 - 1  → [-1, 1] */
    for (int i = 0; i < POLICY_INPUT_DIM; i++) {
        float lo = OBS_LOW[i], hi = OBS_HIGH[i];
        buf0[i] = (obs_raw[i] - lo) / (hi - lo + 1e-6f) * 2.0f - 1.0f;
        if (buf0[i] < -1.0f) buf0[i] = -1.0f;
        if (buf0[i] >  1.0f) buf0[i] =  1.0f;
    }

    const float* in_ptr = buf0;
    int in_size = POLICY_INPUT_DIM;

    /* Katman 0: 7 → 128, tanh */
    {
        float* out_ptr = buf1;
        int out_size   = 128;
        for (int j = 0; j < out_size; j++) {
            float acc = layer0_b[j];
            for (int k = 0; k < in_size; k++)
                acc += layer0_W[j * in_size + k] * in_ptr[k];
            out_ptr[j] = tanhf(acc);
        }
        in_ptr  = out_ptr;
        in_size = out_size;
    }

    /* Katman 1: 128 → 64, tanh */
    {
        float* out_ptr = buf1;
        int out_size   = 64;
        for (int j = 0; j < out_size; j++) {
            float acc = layer1_b[j];
            for (int k = 0; k < in_size; k++)
                acc += layer1_W[j * in_size + k] * in_ptr[k];
            out_ptr[j] = tanhf(acc);
        }
        in_ptr  = out_ptr;
        in_size = out_size;
    }

    /* Çıkış katmanı: 64 → 4, linear */
    {
        for (int j = 0; j < NUM_STRATEGIES; j++) {
            float acc = layer2_b[j];
            for (int k = 0; k < in_size; k++)
                acc += layer2_W[j * in_size + k] * in_ptr[k];
            out_probs[j] = acc;
        }
    }

    /* Softmax */
    float max_v = out_probs[0];
    for (int i = 1; i < NUM_STRATEGIES; i++)
        if (out_probs[i] > max_v) max_v = out_probs[i];
    float sum = 0.0f;
    for (int i = 0; i < NUM_STRATEGIES; i++) {
        out_probs[i] = expf(out_probs[i] - max_v);
        sum += out_probs[i];
    }
    for (int i = 0; i < NUM_STRATEGIES; i++)
        out_probs[i] /= sum;
}

/* argmax yardımcı fonksiyon */
static int policy_argmax(const float* probs) {
    int best = 0;
    for (int i = 1; i < NUM_STRATEGIES; i++)
        if (probs[i] > probs[best]) best = i;
    return best;
}
